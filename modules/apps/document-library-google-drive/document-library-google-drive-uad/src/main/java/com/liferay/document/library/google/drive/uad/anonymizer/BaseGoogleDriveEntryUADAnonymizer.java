package com.liferay.document.library.google.drive.uad.anonymizer;

import com.liferay.document.library.google.drive.model.GoogleDriveEntry;
import com.liferay.document.library.google.drive.service.GoogleDriveEntryLocalService;
import com.liferay.document.library.google.drive.uad.constants.GoogleDriveUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the google drive entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link GoogleDriveEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseGoogleDriveEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<GoogleDriveEntry> {
	@Override
	public void autoAnonymize(GoogleDriveEntry googleDriveEntry, long userId,
		User anonymousUser) throws PortalException {
		if (googleDriveEntry.getUserId() == userId) {
			googleDriveEntry.setUserId(anonymousUser.getUserId());
			googleDriveEntry.setUserName(anonymousUser.getFullName());
		}

		googleDriveEntryLocalService.updateGoogleDriveEntry(googleDriveEntry);
	}

	@Override
	public void delete(GoogleDriveEntry googleDriveEntry)
		throws PortalException {
		googleDriveEntryLocalService.deleteGoogleDriveEntry(googleDriveEntry);
	}

	@Override
	public Class<GoogleDriveEntry> getTypeClass() {
		return GoogleDriveEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return googleDriveEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return GoogleDriveUADConstants.USER_ID_FIELD_NAMES_GOOGLE_DRIVE_ENTRY;
	}

	@Reference
	protected GoogleDriveEntryLocalService googleDriveEntryLocalService;
}